public class arth {
    public static void main(String[] args) {
        int a=1;
        int b=3;
        int c=4;
        int d=c-a;
        int e=--d;
        int f=++d;
        System.out.println("a+a= "+(a+a));
        System.out.println("a*b= "+(a*b));
        System.out.println("b/c= "+(b/c));
        System.out.println("c-a= "+(d));
        System.out.println("--d= "+(e));
        System.out.println("++d= "+(f));
 
    }
    
}
